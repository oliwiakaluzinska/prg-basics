def  month(n):
    if n == 1:
        return 'January'
    elif n ==2:
        return 'February'
    elif n == 3:
        return 'March'
    elif n == 4:
        return "April"
    elif n == 5:
        return 'May'
    elif n == 6:
        return 'June'

