number = int(input('Enter the number: '))
countdown = number
while countdown > 0:
    print(countdown)
    countdown -= 1
    if countdown == 5:
        countdown == 'five'
    elif countdown == 4:
        countdown == 'four'
    elif countdown == 3:
        countdown == 'three'
    elif countdown == 2:
        countdown == 'two'
    elif countdown == 1:
        countdown == 'one'